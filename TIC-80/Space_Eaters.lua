--Blue Power-Up = Spread Shot. Green powerup makes shots bigger.
--Orange Power-Up = Adds mini ship, green powerup makes it's shots bigger.
--Rocks Power-Up = gives ability to destroy rocks

--Heart = +1 Ship
--Bomb = +1 Bomb

t=0
x=96
y=24

function TIC()

	if btn(0) then y=y-1 end
	if btn(1) then y=y+1 end
	if btn(2) then x=x-1 end
	if btn(3) then x=x+1 end

	cls(13)
	spr(1+t%60//30*2,x,y,14,3,0,0,2,2)
	print("HELLO WORLD!",84,84)
	t=t+1
end

